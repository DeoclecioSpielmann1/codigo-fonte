//Comparar duas variáveis
//Declare duas variáveis 
//e utilize operadores de comparação (>, <, ==, !=) para verificar se elas são iguais, diferentes, maior ou menor.
let variavel1 = 8;
let variavel2 = 16;
let saoIguais = variavel1 == variavel2;
let saoDiferentes = variavel1 != variavel2;
let Maior = variavel1 > variavel2;
let Menor = variavel1 < variavel2;
console.log("As variáveis são iguais? " + saoIguais);
console.log("As variáveis são diferentes? " + saoDiferentes);
console.log("A variável 1 é maior que a variável 2? " + Maior);
console.log("A variável 1 é menor que a variável 2? " + Menor);